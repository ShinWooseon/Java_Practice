package com.question;

/* 이름(name), 전화번호(tel) 
*/

import java.util.*;


public class Phone {
	private String name;
	private String tel;

	public Phone(String name, String tel) {
		this.name = name;
		this.tel = tel;
		
	}

	public String getName() {
		return name;
	}

	public String getTel() {
		return tel;
	}
	
	
}

