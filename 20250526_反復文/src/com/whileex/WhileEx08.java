package com.whileex;


import java.util.*;

public class WhileEx08 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		int num ;
		
		do {
			System.out.println("실행");
			
			System.out.print("정수 입력 : ");
			num = sc.nextInt();
			
		} while ( num == 0);
		
		
	}
}
