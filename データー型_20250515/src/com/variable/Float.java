package com.variable;

// float : 実数のデーター型(4byte)
// 		   実数型の基本データー型はdouble

public class Float {

	public static void main(String[] args) {

		float var1;
		var1 = 3.4f;
		double var2;
		var2 = 55.55;
		
		System.out.println("var1 :"+ var1);
		System.out.println("var2 :"+ var2);
		System.out.printf("var1 : %f", var1);
		System.out.println();
		System.out.printf("var2 : %f", var2);
		
	}

}
